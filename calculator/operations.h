#pragma once
void plus(double num1, double num2) 
{
	std::cout << "Result = " << num1 + num2 << std::endl;
}

void minus(double num1, double num2)
{
	std::cout << "Result = " << num1 - num2 << std::endl;
}

void multiply(double num1, double num2)
{
	std::cout << "Result = " << num1 * num2 << std::endl;
}

void divide(double num1, double num2)
{
	std::cout << "Result = " << num1 / num2 << std::endl;
}
